package util;

public class InputHelper {
    // Bu sınıf, kullanıcıdan veri almak için yardımcı işlevler sunacak şekilde tasarlanabilir.
    // Kullanıcıdan giriş alırken doğrulama yapabilir ve hatalı girişleri engelleyebilir.

    // Örneğin, kullanıcıdan bir metin almak için getString() metodu eklenebilir.
    // getString() metodu kullanıcıdan metin alır ve yanlış bir giriş varsa tekrar sorar.

    // Benzer şekilde, kullanıcıdan bir tam sayı almak için getInt() metodu eklenebilir.
    // getInt() metodu, geçerli bir tam sayı alana kadar kullanıcının girişini kontrol eder.

    // Ayrıca, kullanıcıdan bir ondalıklı sayı (double) almak için getDouble() metodu da eklenebilir.
    // getDouble() metodu, geçerli bir ondalıklı sayı girilene kadar tekrar giriş alır.

    // Bunlar, kullanıcıdan gelen verilerin doğruluğunu sağlamak amacıyla kullanılabilir.

    // Bu sınıf, kullanıcı etkileşimli uygulamalarda veri girişini daha güvenli ve kullanıcı dostu hale getirmek için faydalıdır.
}
